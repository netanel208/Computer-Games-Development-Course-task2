using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class TimedSpawnerRandom: MonoBehaviour {
    [SerializeField] Mover prefabToSpawn;
    [SerializeField] Vector3 velocityOfSpawnedObject;
    [Tooltip("Minimum time between consecutive spawns, in seconds")] [SerializeField] float minTimeBetweenSpawns = 1f;
    [Tooltip("Maximum time between consecutive spawns, in seconds")] [SerializeField] float maxTimeBetweenSpawns = 3f;
    [Tooltip("Maximum distance in X between spawner and spawned objects, in units")] [SerializeField] float maxXDistance = 0.5f;

    // Start is called before the first frame update
    void Start() {
        this.StartCoroutine(SpawnRoutine());
    }

    private IEnumerator SpawnRoutine() {
        while (true) {
            float timeBetweenSpawns = Random.Range(minTimeBetweenSpawns, maxTimeBetweenSpawns);
            yield return new WaitForSeconds(timeBetweenSpawns);
            Vector3 positionOfSpawnedObject = new Vector3(
                transform.position.x + Random.Range(-maxXDistance, +maxXDistance),
                transform.position.y,
                transform.position.z);
            GameObject newObject = Instantiate(prefabToSpawn.gameObject, positionOfSpawnedObject, Quaternion.identity);
            newObject.GetComponent<Mover>().SetVelocity(velocityOfSpawnedObject);
        }
    }

    public void SetVelocityOfSpawnedObject(Vector3 vec)
    {
        this.velocityOfSpawnedObject = vec;
    }

    public void SetmaxTimeBetweenSpawns(float time)
    {
        this.maxTimeBetweenSpawns = time;
    }

    public float GetaxTimeBetweenSpawns()
    {
        return maxTimeBetweenSpawns;
    }

    public Vector3 GetVelocityOfSpawnedObject()
    {
        return velocityOfSpawnedObject;
    }

}
